class TSCSSRule
{
	/**************************************************************************/
	
	static function create()
	{
		global $ts_rule;
		
		if(!is_array($ts_rule)) $ts_rule=array();
		
		if(!array_key_exists('panel',$ts_rule))
			$ts_rule['panel']=array();
		if(!array_key_exists('menu',$ts_rule))
			$ts_rule['menu']=array();		
	}
	
	/**************************************************************************/
	
	static function addMenuItem($menu)
	{
		global $ts_rule;
		self::create();
		array_push($ts_rule['menu'],$menu);
	}

	/**************************************************************************/
	
	static function addPanel($panel_id,$rule)
	{
		global $ts_rule;
		self::create();
		$ts_rule['panel'][$panel_id]=$rule;
	}
	
	/**************************************************************************/
}

