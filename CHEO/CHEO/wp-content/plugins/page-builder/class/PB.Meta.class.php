class PBMeta
{
	/**************************************************************************/
	
	static function get($post)
	{
		return(get_post_meta(self::getId($post),PLUGIN_PAGE_BUILDER_META,true));
	}
	
	/**************************************************************************/
	
	static function update($post,$meta)
	{
		update_post_meta(self::getId($post),PLUGIN_PAGE_BUILDER_META,$meta);
	}
	
	/**************************************************************************/
	
	static function getId($post)
	{
		return(is_object($post) ? $post->ID : (int)$post);
	}

	/**************************************************************************/
}

