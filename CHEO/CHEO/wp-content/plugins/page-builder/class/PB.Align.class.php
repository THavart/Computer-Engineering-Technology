class PBAlign
{
	/**************************************************************************/

	function __construct() 
	{ 
		$this->align=array
		(
			'left'																=>	array(__('Left',PLUGIN_PAGE_BUILDER_DOMAIN)),
			'center'															=>	array(__('Center',PLUGIN_PAGE_BUILDER_DOMAIN)),
			'right'																=>	array(__('Right',PLUGIN_PAGE_BUILDER_DOMAIN))
		);
	}
	
	/**************************************************************************/
	
	function getCSSClass($align)
	{
		return('align'.$align);
	}

	/**************************************************************************/
}

