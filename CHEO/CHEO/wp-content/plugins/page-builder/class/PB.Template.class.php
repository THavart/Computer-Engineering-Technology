class PBTemplate
{
	/**************************************************************************/
	
	function __construct($data,$path)
	{
		$this->data=$data;
		$this->path=$path;
	}

	/**************************************************************************/

	function output($format=false)
	{
		ob_start();
 		include($this->path);
		$value=ob_get_clean();
		if($format) $value=PBHelper::formatCode($value);
		return($value);
	}

	/**************************************************************************/
}

