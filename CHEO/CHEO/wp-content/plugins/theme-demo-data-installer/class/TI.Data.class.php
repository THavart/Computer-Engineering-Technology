<?php

/******************************************************************************/
/******************************************************************************/

class TIData
{
	/**************************************************************************/
	
	static function create()
	{
		global $ti_data;
		if(!is_array($ti_data)) $ti_data=array();		
	}
	
	/**************************************************************************/
	
	static function set($key,$name,$value)
	{
		global $ti_data;
		TIData::create();
		$ti_data[$key][$name]=$value;
	}

	/**************************************************************************/
}

