class ThemeGlobalData
{
	/**************************************************************************/
	
	function __construct()
	{
		
		
	}
	
	/**************************************************************************/
	
	function setGlobalData($name,$functionCallback,$refresh=false)
	{
		global $fable_globalData;
		
		if(isset($fable_globalData[$name]) && (!$refresh)) return($fable_globalData[$name]);
		
		$fable_globalData[$name]=call_user_func($functionCallback);
		
		return($fable_globalData[$name]);
	}

	/**************************************************************************/
}

